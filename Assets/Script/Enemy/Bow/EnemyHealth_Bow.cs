using System.Collections;
using System.Collections.Generic;
using UnityEngine;

public class EnemyHealth_Bow : ObjectHealth
{
     private EnemyController controller;

    // 처형 가능 표시
    [SerializeField] private GameObject deathDot;

    // 히트 오디오소스
    [SerializeField] private AudioSource hitAudio;

    // 히트 사운드
    [SerializeField] private AudioClip hitSound;

    // 체력 UI
    [SerializeField] private GameObject healthUI;

    public override void Awake()
    {
        base.Awake();
        controller = GetComponent<EnemyController>();
    }
    private void Update()
    {
        Excution();
    }

    // 플레이어에게 공격 받음
    public override void Hit(int DMG)
    {
        // 현재 상태가 이미 사망한 상태면 피격 처리하지 않음
        if (health <= 0) return;

        // 체력 감소
        health -= DMG;
        posture += 25;

        // 히트 소리 재생
        hitAudio.clip = hitSound;
        hitAudio.Play();

        // 피격 상태로 전환
        controller.TransactionToState(4);
    }

    public override void Death()
    {
        // 처형 애니메이션 상태로 전환
        health = 0;
        isDead = true;
        controller.TransactionToState(5);
        healthUI.SetActive(false);
        return;
    }

    public void Excution()
    {
        if (health > 0)
        {
            if (posture >= 100)
            {
                posture = 100;
                deathDot.SetActive(true);
                return;
            }

            deathDot.SetActive(false);
        }
        else
        {
            if (isDead)
            {
                deathDot.SetActive(false);
            }
            else
            {
                deathDot.SetActive(true);
            }

        }
    }

    public override void postureBreak()
    {
        // 궁병은 구현할 필요 없음
    }
}
